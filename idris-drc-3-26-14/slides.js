var currentSlide = 0;

function setByTime(t) {
  var i = 0;
  while (i < slides.length && t >= slides[i].time) {
    i++;
  }
  if (i >= slides.length || slides[i].time > t) {
    i--;
  }
  setSlide(i);
}

function setByName(n) {
  for (var i = 0; i < slides.length; i++) {
    if (slides[i].name == n) {
      setSlide(i);
    }
  }
}

function elementForSlide(s) {
  if (s.type == "pdf") {
     return document.getElementById("pdf");
  } else if (s.type == "code") {
     return document.getElementById(s.code);
  }
}

function setSlide(i) {
  if (i == currentSlide) {
    return;
  }
  elementForSlide(slides[currentSlide]).style.display = "none";
  currentSlide = i;
  var s = slides[i];
  var el = elementForSlide(s);
  el.style.display = "block";
  
  if (s.type == "pdf") {
    var newel = el.cloneNode(true);
    newel.src = "idris." + s.page + ".pdf";
    el.parentNode.replaceChild(newel, el);
  } else if (s.type == "code") {
    document.getElementsByName(s.cloc)[0].scrollIntoView();
  }
}
